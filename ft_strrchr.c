#include <stdlib.h>

char	*ft_strrchr(const char *str, int c)
{
	char *buf;

	buf = (char*)str;
	while (*buf)
		buf++;
	while (buf >= str)
	{
		if (*buf == (char)c)
			return (char*)(buf);
		buf--;
	}
	return (NULL);
}
