#include "libft.h"

void	ft_lstdelone(t_list **lst, void (*fnc)(void*, size_t))
{
	if (!lst || !*lst || !fnc)
		return ;
	fnc((*lst)->content, (*lst)->content_size);
	ft_free(*lst);
	*lst = NULL;
}
