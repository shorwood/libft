int		ft_strcmp(const char *str, const char *cmp)
{
	while (*str || *cmp)
	{
		if (*str != *cmp)
			return ((unsigned char)*str - (unsigned char)*cmp);
		str++;
		cmp++;
	}
	return (0);
}
