#include <stdlib.h>

char	*ft_strnew(size_t len)
{
	void *str;

	if (!(str = (char*)malloc(++len * sizeof(char))))
		return (str);
	while (len--)
		*(char*)(str + len) = '\0';
	return (str);
}
