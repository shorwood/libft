#include "libft.h"

void	*ft_memchr(const void *ptr, int byt, size_t len)
{
	while (len--)
	{
		if (*(uint8_t*)ptr == (uint8_t)byt)
			return (void*)(ptr);
		ptr++;
	}
	return (NULL);
}
