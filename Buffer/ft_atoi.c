int		ft_atoi(char *str)
{
	int	x;
	int	s;

	if (!str)
		return (0);
	x = 0;
	s = 1;
	while (*str == '\t' || *str == '\v' || *str == '\n'
		|| *str == '\r' || *str == '\f' || *str == ' ')
		str++;
	if (*str == '+')
		str++;
	if (*str == '-')
	{
		s = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
		x = x * 10 + *str++ - '0';
	return (x * s);
}
