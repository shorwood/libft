#include <stdlib.h>

char	*ft_strcpy(char *dst, char *src)
{
	char *buf;

	if (!dst || !src)
		return (dst);
	buf = dst;
	while (*src)
		*buf++ = *src++;
	*buf = '\0';
	return (dst);
}
