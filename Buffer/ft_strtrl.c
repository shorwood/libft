void	ft_strtrl(char *str, char *c, char *r)
{
	if (!str || !c || !r || !*c || !*r)
		return ;
	ft_strtrl(str, c + 1, r + 1);
	while (*str)
		if (*str == *c)
			*str++ = *r;
		else
			str++;
}
