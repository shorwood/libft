#include <stdlib.h>

int		ft_strnequ(const char *str, const char *equ, size_t len)
{
	if (!str || !equ)
		return (0);
	while ((*str || *equ) && len--)
		if (*str++ != *equ++)
			return (0);
	return (1);
}
