int		ft_atoi(const char *str)
{
	int	x;
	int	s;

	x = 0;
	s = 1;
	while ((*str >= '\t' && *str <= '\r') || *str == ' ')
		str++;
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		s = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
		x = x * 10 + *str++ - '0';
	return (x * s);
}
