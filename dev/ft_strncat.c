char	*ft_strncat(char *dst, char *src, int n)
{
	char *buf;

	buf = dst;
	while (*buf)
		buf++;
	while (*src && n)
	{
		*buf = *src;
		buf++;
		src++;
		n--;
	}
	*buf = '\0';
	return (dst);
}
