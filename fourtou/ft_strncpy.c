char	*ft_strncpy(char *dst, char *src, unsigned int n)
{
	char *dst_s;

	dst_s = dst;
	while (*src && n)
	{
		*dst = *src;
		dst++;
		src++;
		n--;
	}
	return (dst_s);
}
