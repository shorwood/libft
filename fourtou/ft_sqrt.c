int		absolute(int n)
{
	if (n < 0)
		return (-n);
	return (n);
}

int		ft_sqrt(int n)
{
	int i;

	i = 1;
	while (i <= n)
	{
		if (i * i == n)
			return (i);
		i++;
	}
	return (0);
}
