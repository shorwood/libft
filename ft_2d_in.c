#include "ft.h"

char	ft_2d_in(t_2d vtx, t_2d a, t_2d b)
{
	if (vtx.x > a.x
		&& vtx.x < b.x
		&& vtx.y > a.y
		&& vtx.y < b.y)
		return (1);
	return (0);
}
