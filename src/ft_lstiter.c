#include "libft.h"

void	ft_lstiter(t_list **lst, void (*fnc)(void *data))
{
	t_list *cur;

	if (!lst || !fnc)
		return ;
	cur = *lst;
	while (cur)
	{
		fnc(cur->data);
		cur = cur->next;
	}
}
