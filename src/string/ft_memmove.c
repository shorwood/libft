#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	uint8_t *buf;

	buf = dst;
	if (dst < src)
		while (len--)
			*(uint8_t*)buf++ = *(uint8_t*)src++;
	else
		while (len--)
			*(uint8_t*)(buf + len) = *(uint8_t*)(src + len);
	return (dst);
}
