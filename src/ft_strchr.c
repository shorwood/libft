#include <stdlib.h>

char	*ft_strchr(const char *str, int chr)
{
	char	c;

	if (!str)
		return (NULL);
	c = (char)chr;
	while (*str && *str != c)
		str++;
	return (*str == c ? (char*)str : NULL);
}
