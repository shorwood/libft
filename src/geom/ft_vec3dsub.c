#include "libft.h"

t_vec3d		ft_vec3dsub(t_vec3d v0, t_vec3d v1)
{
	return ((t_vec3d){ .x = v0.x - v1.x, .y = v0.y - v1.y, .z = v0.z - v1.z });
}
